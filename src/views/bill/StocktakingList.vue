<script>
  import OtherInModal from './modules/OtherInModal'
  import BillDetail from './dialog/BillDetail'
  import BillExcelIframe from '@/components/tools/BillExcelIframe'
  import BatchWaitBillList from './dialog/BatchWaitBillList'
  import { JeecgListMixin } from '@/mixins/JeecgListMixin'
  import { BillListMixin } from './mixins/BillListMixin'
  import JEllipsis from '@/components/jeecg/JEllipsis'
  import JDate from '@/components/jeecg/JDate'
  import { deleteAction } from '@/api/manage'
  export default {
    name: "StocktakingList",
    mixins:[JeecgListMixin,BillListMixin],
    components: {
      OtherInModal,
      BillDetail,
      BillExcelIframe,
      BatchWaitBillList,
      JEllipsis,
      JDate,
      VNodes: {
        functional: true,
        render: (h, ctx) => ctx.props.vnodes,
      }
    },
    data () {
      return {
        // 查询条件
        queryParam: {
          number: "",
          materialParam: "",
          type: "盘点",
          subType: "盘点",
          organId: undefined,
          depotId: undefined,
          creator: undefined,
          linkNumber: "",
          status: undefined,
          remark: ""
        },
        prefixNo: 'PD',
        urlPath: '/bill/other_in',
        //出入库管理开关，适合独立仓管场景
        inOutManageFlag: false,
        labelCol: {
          span: 5
        },
        wrapperCol: {
          span: 18,
          offset: 1
        },
        // 默认索引
        defDataIndex:['action','organName','number','materialsList','operTimeStr','userName','materialCount','totalPrice','status'],
        // 默认列
        defColumns: [
          {
            title: '操作',
            dataIndex: 'action',
            align:"center", width: 160,
            scopedSlots: { customRender: 'action' },
          },
          { title: '供应商', dataIndex: 'organName',width:120, ellipsis:true},
          { title: '单据编号', dataIndex: 'number',width:160,
            customRender:function (text,record,index) {
              text = record.linkNumber?text+"[转]":text
              return text
            }
          },
          { title: '关联单据', dataIndex: 'linkNumber',width:140},
          { title: '商品信息', dataIndex: 'materialsList',width:220, ellipsis:true},
          { title: '单据日期', dataIndex: 'operTimeStr',width:145},
          { title: '操作员', dataIndex: 'userName',width:80, ellipsis:true},
          { title: '数量', dataIndex: 'materialCount',width:60},
          { title: '金额合计', dataIndex: 'totalPrice',width:80},
          { title: '备注', dataIndex: 'remark',width:200},
          { title: '状态', dataIndex: 'status', width: 80, align: "center",
            scopedSlots: { customRender: 'customRenderStatus' }
          }
        ],
        url: {
          list: "/depotHead/list",
          delete: "/depotHead/delete",
          deleteBatch: "/depotHead/deleteBatch",
          batchSetStatusUrl: "/depotHead/batchSetStatus"
        }
      }
    },
    computed: {
    },
    created() {
      this.initSystemConfig()
      this.initSupplier()
      this.getDepotData()
      this.initUser()
      // 盘点单不需要待入库统计
      // this.initWaitBillCount('盘点', '盘点', '1,3')
    },
    methods: {
      // 重写新增方法，设置正确的type和subType
      myHandleAdd() {
        this.$refs.modalForm.action = "add";
        if(this.btnEnableList.indexOf(2)===-1) {
          this.$refs.modalForm.isCanCheck = false
        }
        // 设置盘点单的type和subType以及prefixNo
        this.$refs.modalForm.billType = '盘点'
        this.$refs.modalForm.billSubType = '盘点'
        this.$refs.modalForm.prefixNo = 'PD'
        this.handleAdd();
      },
      // 重写复制新增方法，设置正确的type和subType
      myHandleCopyAdd(record) {
        this.$refs.modalForm.action = "copyAdd";
        if(this.btnEnableList.indexOf(2)===-1) {
          this.$refs.modalForm.isCanCheck = false
        }
        //复制单据的时候需要移除关联单据的相关信息
        record.linkNumber = ''
        record.billType = ''
        record.deposit = ''
        // 设置盘点单的type和subType以及prefixNo
        this.$refs.modalForm.billType = '盘点'
        this.$refs.modalForm.billSubType = '盘点'
        this.$refs.modalForm.prefixNo = 'PD'
        this.$refs.modalForm.edit(record);
        this.$refs.modalForm.title = "复制新增";
        this.$refs.modalForm.disableSubmit = false;
        //开启明细的编辑模式
        this.$refs.modalForm.rowCanEdit = true
        let columnIndex = record.subType === '组装单' || record.subType === '拆卸单'?2:1
        const FormTypes = require('@/utils/JEditableTableUtil').FormTypes
        this.$refs.modalForm.materialTable.columns[columnIndex].type = FormTypes.popupJsh
      },
      // 重写编辑方法，设置正确的type和subType
      myHandleEdit(record) {
        if(record.status === '0') {
          this.$refs.modalForm.action = "edit";
          if(this.btnEnableList.indexOf(2)===-1) {
            this.$refs.modalForm.isCanCheck = false
          }
          // 设置盘点单的type和subType以及prefixNo
          this.$refs.modalForm.billType = '盘点'
          this.$refs.modalForm.billSubType = '盘点'
          this.$refs.modalForm.prefixNo = 'PD'
          //查询单条单据信息
          const { findBillDetailByNumber } = require('@/api/api')
          findBillDetailByNumber({ number: record.number }).then((res) => {
            if (res && res.code === 200) {
              let item = res.data
              this.handleEdit(item)
            }
          })
        } else {
          this.$message.warning("抱歉，只有未审核的单据才能编辑，请先进行反审核！")
        }
      },
      searchQuery() {
        this.loadData(1)
        // 盘点单不需要待入库统计
        // if(this.inOutManageFlag) {
        //   this.initWaitBillCount('盘点', '盘点', '1,3')
        // }
      },
      searchReset() {
        this.queryParam = {
          type: "盘点",
          subType: "盘点"
        }
        this.loadData(1)
        // 盘点单不需要待入库统计
        // if(this.inOutManageFlag) {
        //   this.initWaitBillCount('盘点', '盘点', '1,3')
        // }
      },
      myHandleDelete(record) {
        let that = this
        if(record.status === '0') {
          deleteAction(that.url.delete, {id: record.id}).then((res) => {
            if(res.code === 200){
              that.loadData(1)
              // 盘点单不需要待入库统计
              // if(that.inOutManageFlag) {
              //   that.initWaitBillCount('盘点', '盘点', '1,3')
              // }
            } else {
              that.$message.warning(res.data.message);
            }
          })
        } else {
          this.$message.warning("抱歉，只有未审核的单据才能删除，请先进行反审核！")
        }
      },
      batchDel: function () {
        if (this.selectedRowKeys.length <= 0) {
          this.$message.warning('请选择一条记录！')
        } else {
          let ids = ""
          for (let i = 0; i < this.selectedRowKeys.length; i++) {
            ids += this.selectedRowKeys[i] + ","
          }
          let that = this
          this.$confirm({
            title: "确认删除",
            content: "是否删除选中数据?",
            onOk: function () {
              that.loading = true
              deleteAction(that.url.deleteBatch, {ids: ids}).then((res) => {
                if(res.code === 200){
                  that.loadData()
                  that.onClearSelected()
                  // 盘点单不需要待入库统计
                  // if(that.inOutManageFlag) {
                  //   that.initWaitBillCount('盘点', '盘点', '1,3')
                  // }
                } else {
                  that.$message.warning(res.data.message)
                }
              }).finally(() => {
                that.loading = false
              })
            }
          })
        }
      },
      //待入库（盘点单不需要此功能）
      handleWaitBill() {
        this.$message.info('盘点单不支持待入库功能')
        // this.$refs.batchWaitBill.show('盘点', '盘点', "1,3")
        // this.$refs.batchWaitBill.title = "批量选择盘点单据"
      },
      waitModalFormClose() {
        this.loadData()
        // this.initWaitBillCount('盘点', '盘点', '1,3')
      },
    }
  }
</script>
